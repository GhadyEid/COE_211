package StaffManagement;
public class Employee {
	public String firstN;
	public String lastN;
	public int age;
	public double salary;
	
	public Employee(String firstN, String lastN, int age, double salary) {
		this.firstN = firstN;
		this.lastN = lastN;
		this.age = age;
		this.salary = salary;
		
	}

}
